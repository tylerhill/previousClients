<p><strong><a name="whatis"></a>What is RememberWell?</strong></p>
<p>RememberWell.net is an online memorial website for you to share and celebrate the memories of your loved one with family and friends forever. Additionally, we have memorial poetry and resources for home funerals, green cemeteries or natural burial and celebrant information.</p>
<p>&nbsp;</p>
<p><strong><a name="services"></a>What services do you offer?</strong></p>
<p>RememberWell offers a template for a user create online memorials as well as obtain free resoures focused on &quot;green&quot; or eco burial..</p>
<p>&nbsp;</p>
<p><strong><a name="whatisamemorial"></a>What is a RememberWell Memorial?</strong></p>
<p>A RememberWell memorial is a page, created by you, dedicated to a particular person. These pages can be customized visually, can contain information about the individual, a guestbook, and a space for offerings left by visitors of the memorial.</p>
<p>&nbsp;</p>
<p><strong><a name="setup"></a>Is it difficult to set up a RememberWell Memorial?</strong></p>
<p>We have tried to make the process of creating a memorial as easy as possible, and we will continue making improvements. We believe that anyone should be able to create a memorial page.</p>
<p>&nbsp;</p>
<p><strong><a name="privacypolicy"></a>Do you have a privacy policy?</strong></p>
<p>We do have a privacy policy. You can read it here.</p>
<p>&nbsp;</p>
<p><strong><a name="terms"></a>Are there terms and conditions to using the site?</strong></p>
<p>You can read our <a href="terms.php">Terms of Use</a> here.</p>
<p>&nbsp;</p>
<p><strong><a name="signingup"></a>Do I have to sign up before I can create a memorial?</strong></p>
<p>Yes. We require you to create an account before you create a memorial or leave messages in memorial guestbooks. We do this to protect the integrity of the memorials on the site.</p>
<p>&nbsp;</p>
<p><strong><a name="pay"></a>Do I have to pay to sign up?</strong></p>
<p>There is no fee to create an account on RememberWell.</p>
<p>&nbsp;</p>
<p><strong><a name="howsign"></a>How do I sign up?</strong></p>
<p>Go to the sign up page and fill out the form. You will then receive an account confirmation email. Sign in and begin creating your commemoration.</p>
<p>&nbsp;</p>
<p><strong><a name="loginhelp"></a>I created an account, so why can't I log in?</strong></p>
<p>Before you can log into your account you have to &quot;activate&quot; your account by clicking on the activation link contained with your account activation e-mail. You should receive that e-mail shortly after signing up.</p>
<p>&nbsp;</p>
<p><strong><a name="confirmationissue"></a>Why didn't I receive my account confirmation e-mail?</strong></p>
<p>While it usually only takes a few minutes to receive your e-mail, it can take up to an hour or more in some cases. </p>
<p>If you did not receive your activation, first check your Spam or Trash folders in your e-mail to see if your e-mail host thought our e-mail was spam. If you are still certain that you did not get the e-mail, we can send it to you again if you go to this page and enter your e-mail address. </p>
<p>If an hour or so has passed after having us re-send the e-mail and you are still not seeing it, please e-mail us at support@rememberwell.net and we will help you individually.</p>
<p>&nbsp;</p>
<p><strong><a name="memorialinfo"></a>Who creates, edits, and updates the content of any individual memorial?</strong></p>
<p>Once you log into your account, you will have the ability to create a new memorial, or edit existing memorials that you have created. Only you will have the ability to modify the information on the memorial, except for guestbook entries and offerings. Guestbook entries and offerings can be left by anyone who views the memorial, however, you do do have the ability to specify whether a memorial allows guestbook entries and offerings. You will also have the ability to remove guestbook entries.</p>
<p>&nbsp;</p>
<p><strong><a name="memorialcost"></a>Do I have to pay to create a memorial?</strong></p>
<p>As of June 2012, all memorials are $9.99 and remain on our site indefinitely (unlike many other memorial sites which charge a monthy fee or erase sites after a year).</p>
<p>&nbsp;</p>
<p><strong><a name="newmemorial"></a>How do I create a new memorial?</strong></p>
<p>Log into your account using the Login section on the left side of the page. Enter your username (your e-mail address) and password, then click the Go button. Immediately after logging in you will be taken to your account page. On this page you will find a link highlighted in red that allows you to &quot;Create New Memorial&quot;. You can always go back to your account page by clicking on the &quot;Your Account&quot; link just below the main menu at the top left corner of every page.</p>
<p>&nbsp;</p>
<p><strong><a name="memorialcreation"></a>Do I have to create the entire memorial all at once?</strong></p>
<p>Not at all. Once you begin creating a memorial, it is actually not viewable by anyone but yourself until you Publish it using the Publish button, available on the memorial Creation/Edit page. Feel free to customize the page by adding information, photos, and a customized background image, all at your own pace. You can save the memorial by clicking the Save button, log out of the site, and return to the site at any time to resume creating the memorial until you are finished.</p>
<p>&nbsp;</p>
<p><strong><a name="editmemorial"></a>How do I update an existing memorial?</strong></p>
<p>If you started a memorial, or even published one previously, you can still go back and edit it at any time. After you have logged in on your account page, you will see a list of the memorials you have either published or started previously. Click on the &quot;edit&quot; link to the right of a particular memorial to resume creating it or to make changes.</p>
<p>&nbsp;</p>
<p><strong><a name="privatememorial"></a>What if I want to restrict access to a Commemoration to my immediate family or to specific friends or colleagues?</strong></p>
<p>You can restrict access to a memorial you have created by making it &quot;private.&quot; To do this, simply check the box that reads &quot;Make this memorial private&quot; under the Visibility section on the right half of the memorial's edit page. Making a memorial private prevents users from finding the memorial on RememberWell.net through out search option, and it also will not appear on the list of memorials on the Memorials page. You will need to copy the URL that appears just below the &quot;Make this memorial private&quot; option and e-mail it those you wish to view it.</p>
<p>&nbsp;</p>
<p><strong><a name="deletememorial"></a>How can I permanently remove a memorial?</strong></p>
<p>After logging in, on your account page, you will see a list of memorials you have either published or started previously. You can delete any of these permantently by clicking on the &quot;delete&quot; link to the right of a particular memorial. You will be taken to a page to confirm whether you really want to delete it, for after it's gone you can't get it back!</p>
