<h3>RememberWell.net</h3>
<br />
<p><strong>What is it?</strong></p>
<p><br>
  RememberWell is two-fold: an end-of-life library dedicated to &quot;green&quot; and alternative choices plus an online memorial. The site is being designed to help people discover their options and/or the options for a loved one for a dignified, peaceful transition. It's a one-stop library: from pre-planning information like how to choose hospice care or where to persue a degree in thanatology to need-it-now &quot;green&quot; caskets or a &quot;threshold&quot; choir to grief support or hand-made urns RememberWell will make it an easy endeavor. </p>
<p>&nbsp;</p>
<p>The online memorial will allow for service information as well as photos, video, a guestbook and a space to leave offerings. The user will choose if the commemoration is public or private. Yet, unlike with social media, all data and information is never shared or sold behind the scenes. We charge $9.99 per commemoration and as a starting offer, (June 2012) they are up indefinitely.This way, you are able to revisit the site whenever you want and may read again the lovely comments that were made about your loved one.</p>
<p>&nbsp;</p>
<p><strong>Why are we building it?</strong></p>
<p>&nbsp;</p>
<p>At the moment one has to piece together information regarding their end of life choices.  Often times, one waits until one has to &quot;deal with it&quot; to know what their options are. We aim to make discovering the information so easy that instead of it being a &quot;have to&quot; subject, it becomes a &quot;want to&quot; subject. No one wants to die but since we all will, why not be informed and responsible for your exit making it magical rather than a bummer.</p>
